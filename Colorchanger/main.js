let text = document.querySelector("input");

function filter(colors, text) {
  let temp = [];
  for (let i = 0; i < colors.length; i++) {
    let currentColor = colors[i].toLowerCase();
    let currentInput = text.toLowerCase();
    if (currentColor.startsWith(currentInput)) {
      temp.push(colors[i]);
    }
  }
  return temp;
}

function render(colors) {
  document.querySelector("ul").remove();
  let list = document.createElement("ul");
  document.querySelector("div").appendChild(list);
  for (let item of colors) {
    let listItem = document.createElement("li");
    listItem.innerText = item;
    listItem.addEventListener("click", function () {
      document.querySelector(".wrapper").style.backgroundColor = item;
    });
    list.appendChild(listItem);
  }
}

let error = ["Invalid Color"];

function colorPicker(event) {
  document.querySelector(".wrapper").style.backgroundColor = text.value;
  let box = text.value;
  let otherbox = filter(colors, box);

  if (otherbox.length == 0) {
    render(error);
  } else {
    render(otherbox);
  }
}

text.addEventListener("keyup", colorPicker);
